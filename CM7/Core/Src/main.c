	 	  memset((void*)nodo2_sd->intensidades, 0, sizeof(nodo2_sd->intensidades));
	 	  memset((void*)nodo3_sd->intensidades, 0, sizeof(nodo3_sd->intensidades));

	 	  HAL_HSEM_FastTake(HSEM_ID_0);
