void print_debug(const char *msg) { //Funcion de debuggeo, posterior eliminaciÃ³n
    HAL_UART_Transmit(&huart3, (uint8_t*)msg, strlen(msg), 100);
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	uint8_t rx_len = 0;
	if (htim == &htim3) {
		rx_len = LoRa_receive(&myLoRa, rx_package.rx_buffer, LORA_MAX_SIZE);

		if(rx_len > 0) {
			char msg[100];
			sprintf(msg, "RX: %d bytes - ", rx_len);
			print_debug(msg);

			for(uint8_t i=0; i < rx_len; i++) {
				sprintf(msg, "%c", rx_package.rx_buffer[i]);
				print_debug(msg);
			}
			print_debug("\r\n");
		} else {
			print_debug("RX: No data\r\n");
		}
	}

}
